#include "ft_printf.h"

int	ft_atoi(const char **str)
{
	int		res;

	res = 0;
	while (**str >= '0' && **str <= '9')
	{
		res = res * 10 + (**str - '0');
		(*str)++;
	}
	(*str)--;
	return (res);
}
